function getLanguage() {
  return localStorage.getItem('language') || 'zh-CN';
}

function setLanguage(lang) {
  localStorage.setItem('language', lang);
  location.reload();
}

function toggleLanguage() {
  const current = getLanguage();
  const next = current === 'zh-CN' ? 'en-US' : 'zh-CN';
  setLanguage(next);

  // æ›´æ–°æŒ‰é’®æ–‡æœ¬
  const langText = document.getElementById('lang-text');
  if (langText) {
    langText.textContent = next === 'zh-CN' ? 'English' : 'ä¸­æ–‡';
  }
}
function t(key, ...args) {
  const lang = getLanguage();
  let text = translations[lang][key] || key;
  args.forEach((arg, index) => {
    text = text.replace(`{${index}}`, arg);
  });
  return text;
}

function applyTranslations() {
  const lang = getLanguage();
  const t_map = translations[lang];

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (t_map[key]) {
      // Handle input placeholders
      if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
        element.placeholder = t_map[key];
      }
      // Handle textarea placeholders
      else if (element.tagName === 'TEXTAREA' && element.hasAttribute('placeholder')) {
        element.placeholder = t_map[key];
      }
      // Handle title attributes
      else if (element.hasAttribute('title')) {
        element.title = t_map[key];
      }
      // Handle regular text content
      else {
        // å¦‚æœåŒ…å«HTMLæ ‡ç­¾ï¼Œä½¿ç”¨innerHTMLï¼Œå¦åˆ™ä½¿ç”¨textContent
        if (t_map[key].includes('<')) {
          element.innerHTML = t_map[key];
        } else {
          element.textContent = t_map[key];
        }
      }
    }
  });

  if (t_map['title']) {
    document.title = t_map['title'];
  }
}

